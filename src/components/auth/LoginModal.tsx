"use client";

import { useState } from "react";
import { useAuth } from "@/application/auth/AuthProvider";
import { Modal } from "@/components/ui/Modal";
import { Button } from "@/components/ui/Button";
import { Input } from "@/components/ui/Input";
import { useTranslations } from "next-intl";
import { Loader2 } from "lucide-react";

interface LoginModalProps {
  isOpen: boolean;
  onClose: () => void;
  onSwitchToSignup: () => void;
  onSwitchToPasswordReset: () => void;
}

export function LoginModal({ 
  isOpen, 
  onClose, 
  onSwitchToSignup, 
  onSwitchToPasswordReset 
}: LoginModalProps) {
  const { signInWithEmail, signInWithGoogle, loading } = useAuth();
  const t = useTranslations("auth");
  
  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [error, setError] = useState("");
  const [isSubmitting, setIsSubmitting] = useState(false);

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setError("");
    
    // Validation
    if (!email || !password) {
      setError(t("error_invalid_email"));
      return;
    }
    
    setIsSubmitting(true);
    try {
      await signInWithEmail(email, password);
      onClose();
      setEmail("");
      setPassword("");
    } catch (err: any) {
      console.error("Login error:", err);
      if (err.code === "auth/user-not-found") {
        setError(t("error_user_not_found"));
      } else if (err.code === "auth/wrong-password") {
        setError(t("error_wrong_password"));
      } else if (err.code === "auth/invalid-email") {
        setError(t("error_invalid_email"));
      } else {
        setError(t("error_auth_failed"));
      }
    } finally {
      setIsSubmitting(false);
    }
  };

  const handleGoogleLogin = async () => {
    try {
      await signInWithGoogle();
      onClose();
    } catch (err) {
      console.error("Google login error:", err);
      setError(t("error_auth_failed"));
    }
  };

  const handleClose = () => {
    setEmail("");
    setPassword("");
    setError("");
    onClose();
  };

  return (
    <Modal isOpen={isOpen} onClose={handleClose} title={t("login_modal_title")}>
      <form onSubmit={handleSubmit} className="space-y-4">
        <div>
          <label htmlFor="login-email" className="block text-sm font-medium text-foreground mb-1">
            {t("email")}
          </label>
          <Input
            id="login-email"
            type="email"
            value={email}
            onChange={(e) => setEmail(e.target.value)}
            placeholder="user@example.com"
            disabled={isSubmitting || loading}
            className="w-full"
            autoComplete="email"
          />
        </div>

        <div>
          <label htmlFor="login-password" className="block text-sm font-medium text-foreground mb-1">
            {t("password")}
          </label>
          <Input
            id="login-password"
            type="password"
            value={password}
            onChange={(e) => setPassword(e.target.value)}
            placeholder="••••••••"
            disabled={isSubmitting || loading}
            className="w-full"
            autoComplete="current-password"
          />
        </div>

        {error && (
          <div className="text-sm text-red-400 bg-red-500/10 border border-red-500/20 rounded-lg p-3">
            {error}
          </div>
        )}

        <Button 
          type="submit" 
          className="w-full"
          disabled={isSubmitting || loading}
        >
          {isSubmitting ? (
            <>
              <Loader2 className="mr-2 h-4 w-4 animate-spin" />
              {t("loading")}
            </>
          ) : (
            t("login_button")
          )}
        </Button>

        <div className="relative my-4">
          <div className="absolute inset-0 flex items-center">
            <div className="w-full border-t border-white/10"></div>
          </div>
          <div className="relative flex justify-center text-xs uppercase">
            <span className="bg-black/40 px-2 text-muted-foreground">or</span>
          </div>
        </div>

        <Button
          type="button"
          variant="secondary"
          className="w-full bg-white hover:bg-gray-50 text-gray-700 border border-gray-300 font-medium transition-all duration-200 shadow-sm hover:shadow-md"
          onClick={handleGoogleLogin}
          disabled={isSubmitting || loading}
        >
          <svg className="mr-2 h-5 w-5" viewBox="0 0 24 24" xmlns="http://www.w3.org/2000/svg">
            <path
              d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
              fill="#4285F4"
            />
            <path
              d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
              fill="#34A853"
            />
            <path
              d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z"
              fill="#FBBC05"
            />
            <path
              d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
              fill="#EA4335"
            />
          </svg>
          {t("google_login")}
        </Button>

        <div className="flex flex-col gap-2 text-sm text-center">
          <button
            type="button"
            onClick={onSwitchToPasswordReset}
            className="text-primary hover:underline"
            disabled={isSubmitting || loading}
          >
            {t("forgot_password")}
          </button>
          
          <div className="text-muted-foreground">
            {t("no_account")}{" "}
            <button
              type="button"
              onClick={onSwitchToSignup}
              className="text-primary hover:underline font-medium"
              disabled={isSubmitting || loading}
            >
              {t("signup_button")}
            </button>
          </div>
        </div>
      </form>
    </Modal>
  );
}
